package model;

public class BodyPart {
	
	private int x;
	private int y;
	
	public BodyPart(int X, int Y) {
		this.x = X;
		this.y = Y;
	}
	 
	public int getX() {
		return x;
	}
	
	public int getY() {
		return y;
	}
	
	public void setX(int X) {
		this.x = X;
	}
	
	public void setY(int Y) {
		this.y = Y;
	}
	
}
